package com.spring.sample;

public class Calculate {

    public int sum(int a, int b) {
        return a+b;
    }


    public int sub(int a, int b) {
        return a-b;
    }


    public int mul(int a, int b) {
        return a*b;
    }


    public double dev(double a, double b) {
        return a/b;
    }
}
